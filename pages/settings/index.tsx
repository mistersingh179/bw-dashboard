type NumberFormControlPropsType = {
  label: string;
  fieldName: string;
  fieldValue: number;
  updateFn: (label: string, value: number) => void;
  children: ReactNode;
  min?: number;
  max?: number;
};
const NumberFormControl = (props: NumberFormControlPropsType) => {
  const { label, fieldName, fieldValue, updateFn, children } = props;
  let { min, max } = props;
  if (min === undefined) {
    min = 0;
  }
  if (max === undefined) {
    max = 10_000;
  }
  return (
    <FormControl my={5}>
      <HStack>
        <FormLabel minW={"3xs"} mb={0}>
          {label}
        </FormLabel>
        <Box w={"lg"}>
          <NumberInput
            min={min}
            max={max}
            step={1}
            value={fieldValue ? Number(fieldValue) : 0}
            onChange={(val) => {
              updateFn(fieldName, parseInt(val));
            }}
          >
            <NumberInputField />
            <NumberInputStepper>
              <NumberIncrementStepper />
              <NumberDecrementStepper />
            </NumberInputStepper>
          </NumberInput>
        </Box>
      </HStack>
      <FormHelperText my={3} lineHeight={1.5}>
        {children}
      </FormHelperText>
    </FormControl>
  );
};

